# String Slicing

name = "Light,Dark"
print(name[0:5])  # including 0 but not 5

# Length of a string

print(len(name))

# String as an array

print(name[:5])

print(name[:])  # by default, it takes string length

print(name[4])

print(name[0:-3])  # i.e, print(name[0: len(name)-3])

print(name[-3:-1])
