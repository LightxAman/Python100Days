# Python Practice examples on Loops

print("\nExample 1 : access elements of the list one by one")

languages = ['Swift', 'Python', 'Go']

# access elements of the list one by one
for i in languages:
    print(i)

print("\nExample 2 : iterate over each character in language")
language = 'Python'

# iterate over each character in language
for x in language:
    print(x)

print("\nExample 3 : iterate over in range")
# iterate from i = 0 to i = 3
for i in range(4):
    print(i)

print("\nExample 4 : iterating using while loop")
number = 1

while number <= 3:
    print(number)
    number = number + 1
