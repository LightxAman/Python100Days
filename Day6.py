# Operators

# ADD operator
print(15 + 6)

# SUBTRACT operator
print(15 - 6)

# MULTIPLY operator
print(15 * 6)

# DIVISION operator
print(15 / 6)

# FLOOR DIVISION operator - It disregards the floating point value and takes the int.
print(15 // 6)

# MODULUS operator
print(15 % 6)

# EXPONENTIAL operator - Basically is power of.
print(15 ** 6)
